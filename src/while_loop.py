# jadi disini kita akan membahas belajar tentang while loop
# seperti contoh berikut :

data = "" # <= jadi disini kita membuat sebuah variabel data dengan string kosong

while data != "x": # <= lalu kita akan melaukan perulangan, ketika data tidak sama dengan x
    print("Masukkan perulangan") # jika dia false maka dia akan membuat sebuah perulangan tanpa henti
    # sampai benar benar mendapatkan nilai x
    data = input("data : ") # <= jika dia true maka dia akan berhenti.